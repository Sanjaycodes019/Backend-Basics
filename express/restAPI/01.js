// 1. What is an API?
// API (Application Programming Interface) is a way for applications to talk to each other.
// In simple words:
// You build a backend that can send data (like a waiter taking your food order).
// Your frontend (React, Android app, etc.) sends requests to this backend.


// 2. What is REST?
// REST = Representational State Transfer
// REST is a set of rules for building APIs. A RESTful API follows these rules:
// Uses HTTP methods (GET, POST, PUT, DELETE)
// Uses proper URLs
// Returns data in a standard format (mostly JSON)